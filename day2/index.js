
var a = 6; 

var b = 1; 

  
// AND Operation 

document.write("A & B = " + (a & b) + '<br>'); 

  

  
// OR operation 

document.write("A | B = " + (a | b) + '<br>'); 

  
// NOT operation 

document.write("~A = " + (~a) + '<br>'); 

  

  
// Sign Propagating Right Shift 

document.write("A >> B = " + (a >> b) + '<br>'); 

  

  
// Zero Fill Right Shift 

document.write("A >>> B = " + (a >>> b) + '<br>'); 

  

  
// Left Shift 

document.write("A << B = " + (a << b) + '<br>');  
